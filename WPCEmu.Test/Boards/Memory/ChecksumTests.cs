using NUnit.Framework;
using WPCEmu.Boards.Memory;

namespace WPCEmu.Test.Boards.Memory
{
	[TestFixture]
	public class ChecksumTests
	{
		[Test, Order(1)]
		public void CalculateMMHighscoreChecksum()
		{
			TestContext.WriteLine("Checksum: calculate MM Highscore Checksum");

			ushort result = Checksum.checksum16(new byte[] {
				0x42, 0x52, 0x45, 0x0, 0x44, 0x0, 0x0, 0x0,
				0x4C, 0x46, 0x53, 0x0, 0x40, 0x0, 0x0, 0x0,
				0x5A, 0x41, 0x50, 0x0, 0x36, 0x0, 0x0, 0x0,
				0x52, 0x43, 0x46, 0x0, 0x32, 0x0, 0x0, 0x0
			});
			Assert.AreEqual(0xFB8F, result);
		}
	}
}
